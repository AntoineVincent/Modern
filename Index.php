				<p class="intro">Warhammer est un univers imaginaire d’heroic fantasy créé dans les années 1980 par la société Games Workshop et développé pour servir de cadre au jeu de figurines Warhammer Fantasy Battle (1983) ainsi que pour d'autres jeux, notamment le jeu de rôle Warhammer Fantasy Roleplay (1986) et son extension futuriste Warhammer 40,000 (1987).</p>
				<img src="./img/duel.jpg" class="duel" alt="duel" />
				<p class="intro">L'univers de Warhammer est inspiré entre autres par une littérature de fantasy telle qu'on la retrouve dans les récits de J. R. R. Tolkien et Michael Moorcock, mais aussi dans les jeux de rôle plus anciens à petite échelle comme Donjons et Dragons. Au mélange des épopées tolkienites et du chaos de Moorcock s'est ajouté une inspiration historique très forte. Le Vieux Monde de Warhammer fait très largement écho à l'Europe occidentale du XVIe siècle au niveau technologique et hiérarchique, avec une forte réminiscence de la pensée médiévale et son mode de vie sous certains aspects. La carte du vieux monde étant en fait une déformation des 6 continents de notre monde.</p>
				<p class="intro">De nombreux peuples se partagent les territoires du monde de Warhammer. La plupart d'entre eux sont des humains originaires du Vieux Monde, d'Arabie, de Norsca, de Nippon, de Cathay etc. Il existe également de nombreuses autres races non humaines : certaines sont proches des humains biologiquement (comtes-vampires, ogres, halflings) ou physiquement (elfes et nains). D'autres races sont totalement à part, comme les races peaux-vertes (orques et gobelins), les skavens ou les hommes-lézards. En effet beaucoup de races se font la guerre, commandés par de grands chefs de guerre qui les mèneront à la victoire ou à la défaite. Une véritable bataille pour savoir qui dirigera la terre. En général, chaque armée possède un point fort. Certaines armées sont polyvalentes (l'armée de l'Empire en fait partie).</p>
